const Alexa = require('ask-sdk-core');
const AWS = require('aws-sdk');
const IotData = new AWS.IotData({endpoint: 'ag6oftoa7eyp-ats.iot.us-east-2.amazonaws.com'});


function getShadowPromise(params) {
    return new Promise((resolve, reject) => {
        IotData.getThingShadow(params, (err, data) => {
            if (err) {
                console.log(err, err.stack);
                reject('Failed to get thing shadow ${err.errorMessage}');
            } else {
                resolve(JSON.parse(data.payload));
            }
        });
    });
}

const LaunchRequestHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'LaunchRequest';
    },
    handle(handlerInput) {
        const speakOutput = 'Your vehicle is alright';

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt(speakOutput)
            .getResponse();
    }
};

const ShadowParams = {
    thingName: 'Mything2',
};

const StillThereIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'StillThereIntent';
    },
    async handle(handlerInput) {
        var moved = 'unknown';
        await getShadowPromise(ShadowParams)
          .then((result) => moved = result.state.reported.moved);
        console.log(moved);

        var speakOutput = 'Error';
        if (moved == 'yes') {
            speakOutput = 'Your Vehicle is being hijacked';
        } else if (moved == 'no') {
            speakOutput = 'Your Vehicle is still there';
        } else {
            speakOutput = 'I cant verify your vehicle status at the moment, please try again later';
        }

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt(speakOutput)
            .getResponse();
  }
};

const TemperatureIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'TemperatureIntent';
    },
    async handle(handlerInput) {
        var temperature = 'unknown';
        await getShadowPromise(ShadowParams)
          .then((result) => temperature = result.state.reported.temperature);
        console.log(temperature);

        var speakOutput = 'Error';
        let temperature_float = parseFloat(temperature);
        if (temperature_float < 10.00) {
            speakOutput = `Your vehicle is not exposed to the sun at the moment, the actual temperature is ${temperature} Celcius`;
        } else if (temperature_float >= 10.00 && temperature_float < 25.00) {
            speakOutput = `its actually hot in there you should take it to a cooler place, the actual temperature is ${temperature} Celcius`;
        } else {
            speakOutput = `It seems that your vehicle could be exposed to direct sunlight right now, this can damage the vehicle please take it to a cooler place, the actual temperature is ${temperature} Celcius`;
        }

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt(speakOutput)
            .getResponse();
  }
};

